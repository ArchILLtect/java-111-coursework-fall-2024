public class PrimitiveDemo {
    /** create double, float, char to demo some primitives
     * 
     * @param args command line arguments (not used here)
    */
    public static void main(String[] args) {
        double myDouble = 45.99;
        float myFloat = 55.791f;
        char myChar = 'y';

        System.out.println("Here is my myDouble: " + myDouble);
        System.out.println("Here is my myFloat: " + myFloat);
        System.out.println("Here is my myChar: " + myChar);

        System.out.println("Here is my myDouble + myFloat: " + (myDouble + myFloat));
        

    }
}